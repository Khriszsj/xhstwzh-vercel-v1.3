// ── 主题检测 ──────────────────────────────────────────────────────────────────
function detectTopic(text: string): string {
  const lower = text.toLowerCase();
  const scores: Record<string, number> = {};

  for (const [topic, keywords] of Object.entries(TOPIC_KEYWORDS)) {
    scores[topic] = keywords.filter((kw) => lower.includes(kw)).length;
  }

  const best = Object.entries(scores).sort((a, b) => b[1] - a[1])[0];
  return best && best[1] > 0 ? best[0] : "";
}

// ── 关键词提取（白名单词库匹配） ──────────────────────────────────────────────
// 中文无空格分词，纯正则会捕获句子片段。
// 改为：扫描文本中出现了哪些词库词汇，主题词优先，兼顾出现频次。
function extractKeywords(text: string, topic: string): string[] {
  const lower = text.toLowerCase();
  const topicKwSet = new Set(TOPIC_KEYWORDS[topic] ?? []);
  const allKws = Object.values(TOPIC_KEYWORDS).flat();

  // 统计每个词库词汇在文本中的出现次数
  const found: Array<{ word: string; isPrimary: boolean; count: number }> = [];
  const seen = new Set<string>();

  for (const kw of allKws) {
    if (seen.has(kw)) continue;
    seen.add(kw);
    if (!lower.includes(kw)) continue;
    let count = 0;
    let pos = 0;
    while ((pos = lower.indexOf(kw, pos)) !== -1) { count++; pos++; }
    found.push({ word: kw, isPrimary: topicKwSet.has(kw), count });
  }

  // 主题词优先，再按出现频次排序
  found.sort((a, b) => {
    if (a.isPrimary !== b.isPrimary) return a.isPrimary ? -1 : 1;
    return b.count - a.count;
  });

  // 英文词（如 pm、offer）单独补充
  const engMatches = lower.match(/\b[a-z]{2,12}\b/g) ?? [];
  const engFreq = new Map<string, number>();
  for (const w of engMatches) {
    if (!STOPWORDS.has(w)) engFreq.set(w, (engFreq.get(w) ?? 0) + 1);
  }
  const engExtra = [...engFreq.entries()]
    .sort((a, b) => b[1] - a[1])
    .map(([w]) => w)
    .filter(w => !found.some(f => f.word === w));

  const result = [...found.map(f => f.word), ...engExtra];
  return [...new Set(result)].slice(0, 8);
}

// ── 主入口 ────────────────────────────────────────────────────────────────────
export function buildSuggestions(doc: RichDoc): SuggestionResult {
  const plainText = docToPlainText(doc);
  const topic = detectTopic(plainText);
  const keywords = extractKeywords(plainText, topic);

  const core      = keywords[0] ?? "这件事";
  const secondary = keywords[1] ?? "经验";

  const templates = TOPIC_TEMPLATES[topic] ?? DEFAULT_TEMPLATES;
  const titles = templates.map((fn) => fn(core, secondary));

  const keywordTags = keywords.slice(0, 5).map((w) => `#${w}`);
  const topicTags   = TOPIC_TAGS[topic] ?? ["#干货分享", "#生活记录", "#内容创作"];
  const tags = [...new Set([...keywordTags, ...topicTags])].slice(0, 15);

  return { titles, tags };
}
